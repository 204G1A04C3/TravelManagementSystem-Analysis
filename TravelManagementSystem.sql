create database TravelManagementSystemDB;

CREATE TABLE Customers (
    CustomerID INT PRIMARY KEY,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Email VARCHAR(100),
    PhoneNumber VARCHAR(15),
    Address VARCHAR(255)
);

CREATE TABLE TravelPackages (
    PackageID INT PRIMARY KEY,
    PackageName VARCHAR(100),
    Description TEXT,
    DurationDays INT,
    Price DECIMAL(10, 2),
    Destination VARCHAR(100)
);

CREATE TABLE Bookings (
    BookingID INT PRIMARY KEY,
    CustomerID INT,
    PackageID INT,
    BookingDate DATE,
    TravelDate DATE,
    Status VARCHAR(20),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
    FOREIGN KEY (PackageID) REFERENCES TravelPackages(PackageID)
);

CREATE TABLE Agents (
    AgentID INT PRIMARY KEY,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Email VARCHAR(100),
    PhoneNumber VARCHAR(15),
    OfficeLocation VARCHAR(255)
);


CREATE TABLE Payments (
    PaymentID INT PRIMARY KEY,
    BookingID INT,
    PaymentDate DATE,
    Amount DECIMAL(10, 2),
    PaymentMethod VARCHAR(50),
    PaymentStatus VARCHAR(20),
    AgentID INT, -- Added column to reference AgentID
    FOREIGN KEY (BookingID) REFERENCES Bookings(BookingID),
    FOREIGN KEY (AgentID) REFERENCES Agents(AgentID) -- Establishing relationship with Agents table
);

